package Collection_SortGatti;

public class Cat {
    private String name;
    private int weight;

    /* constructor */
    public Cat (String name, int weight) {
        this.name = name;
        this.weight = weight;
    }

    /* getters */
    public String getName() {
        return name;
    }

    public int getWeight() {
        return weight;
    }

    /* setters */
    public void setName(String name) {
        this.name = name;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

}
